                    entgegen ihres Naturells, als Schlafmütze. Danach gab es kein Halten mehr. In wechselnden Rollen,
                    mal als Bauernmädchen, mal als Badenixe á la knallrotes Gummiboot, war sie ab sofort jährlich im
                    Dorfkarneval aktiv.
                    Von 1979 bis 1985 werkelte sie dann auch beim Wagenbau der Pfadfinder mit und präsentierte das
                    Gemeinschaftswerk dann natürlich auch beim Veilchendienstagsumzug. Ob Hühner, Zwerge oder Arche
                    Noah – der Gruppe war kein Thema beim Wagenbau zu schwer.
                    Nach ihrem Abitur 1984 war Ute aufgrund von Ausbildung und Studium nicht mehr aktiv auf der Bühne
                    zu sehen, blieb aber dem Dorfkarneval als Trainerin der Mädchengarde und Teilnehmerin am Umzug treu.
                    Pünktlich zum Millenium schnupperte Ute dann mal in den Neuwieder Karneval hinein und stellte im
                    Rosenmontagszug als Schlafmütze das Schlaflabor vom damaligen Prinz Jörg (Groth) dar.
                    Da Utes Energie keine Grenzen kennt, versuchte sie 2008 erstmalig, den Karneval in ihrem Wohnort
                    Melsbach aufleben zu lassen. So moderierte sie bis 2011 den Melsbacher Kinderkarneval und den
                    Karneval für die Großen in der Elisabethhöhe, später im Gemeindezentrum und der Turnhalle.
                    Im Jahr 2012 rückte sie noch ein Stückchen näher an Neuwied heran und bereicherte den Karnevalsumzug
                    im schönen Irlich.
                    Seit 2017 ist „die gute Ute“ Mitglied in der Ehrengarde-Familie und aus dieser bereits nach so
                    kurzer Zeit nicht mehr wegzudenken. Wann und wo auch immer ein Mitglied wieder bühnenfit gemacht
                    werden oder die Truppe mit ein paar leckeren Häppchen bei Laune gehalten werden muss, ist Ute nicht fern.
                    Ute freut sich auf diese besondere Session und bereichert das Prinzenteam durch ihre unerschütterliche
                    gute Laune.
